<script>
document.addEventListener('DOMContentLoaded', () => {
    const carousel = document.getElementById('default-carousel');
    const items = carousel.querySelectorAll('[data-carousel-item]');
    const totalItems = items.length;

    let currentIndex = 0;

    function resetAnimation(element) {
        element.classList.remove('fadeInBottom');
        void element.offsetWidth; // trigger reflow
        element.classList.add('fadeInBottom');
    }

    function handleCarouselChange() {
        items.forEach((item, index) => {
            if (index === currentIndex) {
                const title = item.querySelector('h4');
                const text = item.querySelector('p');
                resetAnimation(title);
                resetAnimation(text);
            }
        });
    }

    function showNextItem() {
        currentIndex = (currentIndex + 1) % totalItems;
        handleCarouselChange();
    }

    function showPrevItem() {
        currentIndex = (currentIndex - 1 + totalItems) % totalItems;
        handleCarouselChange();
    }

    // Add event listeners for carousel controls
    document.querySelector('[data-carousel-next]').addEventListener('click', showNextItem);
    document.querySelector('[data-carousel-prev]').addEventListener('click', showPrevItem);
    document.querySelectorAll('[data-carousel-slide-to]').forEach((button, index) => {
        button.addEventListener('click', () => {
            currentIndex = index;
            handleCarouselChange();
        });
    });

    // Initial call to set up the first item
    handleCarouselChange();
});
</script>
